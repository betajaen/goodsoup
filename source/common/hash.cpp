	uint32 quickHash(char* str) {
		char* s;
		uint8 h = 0;
		uint16 r = 0;
		uint8 len = 0;

		if (*str == 0)
			return 0;

		for (int i = 0; i < 3; i++) {
			s = str;
			h = HASH_TABLE[((*s) + i) & 0xFF];
			s++;
			len = 0;
			while (*s != 0) {
				h = HASH_TABLE[(h ^ ((*s))) & 0xFF];
				s++;
				len++;
			}

			gs_verbose("(%d, %d, %d)", i, h, r);

			r = r << 8 | h;
			
		}

		r = r << 8 | len;


		return r;
	}
